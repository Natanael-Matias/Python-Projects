# Faça um programa que leia um número inteiro qualquer
# e mostre na tela a sua tabuada.

t = int(input('Digite um número inteiro: '))
print('A tabuada de {} será: \n'.format(t), '{} x 1 ='.format(t), t * 1, '\n',
      '{} x 2 ='.format(t), t * 2, '\n',
      '{} x 3 ='.format(t), t * 3, '\n',
      '{} x 4 ='.format(t), t * 4, '\n',
      '{} x 5 ='.format(t), t * 5, '\n',
      '{} x 6 ='.format(t), t * 6, '\n',
      '{} x 7 ='.format(t), t * 7, '\n',
      '{} x 8 ='.format(t), t * 8, '\n',
      '{} x 9 ='.format(t), t * 9)
